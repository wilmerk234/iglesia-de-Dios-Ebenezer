// Change these values later to match your real temple fund numbers.
const fundraisingData = {
  goal: 50000,
  raised: 18500
};

const languageToggle = document.querySelector(".language-toggle");
const navToggle = document.querySelector(".nav-toggle");
const siteNav = document.querySelector(".site-nav");
const prayerForm = document.getElementById("prayer-form");
const formMessage = document.getElementById("form-message");
const progressFill = document.getElementById("progress-fill");
const progressBar = document.querySelector(".progress-bar");
const goalAmount = document.getElementById("goal-amount");
const raisedAmount = document.getElementById("raised-amount");
const progressText = document.getElementById("progress-text");

let currentLanguage = "en";

function formatCurrency(amount, language) {
  return new Intl.NumberFormat(language === "es" ? "es-US" : "en-US", {
    style: "currency",
    currency: "USD",
    maximumFractionDigits: 0
  }).format(amount);
}

function updateFundraising(language) {
  const percent = Math.min(Math.round((fundraisingData.raised / fundraisingData.goal) * 100), 100);

  goalAmount.textContent = formatCurrency(fundraisingData.goal, language);
  raisedAmount.textContent = formatCurrency(fundraisingData.raised, language);
  progressFill.style.width = `${percent}%`;
  progressBar.setAttribute("aria-valuenow", String(percent));
  progressText.textContent = translations[language].progressText.replace("{percent}", percent);
}

function updatePlaceholders(language) {
  const placeholders = {
    en: {
      name: "Your name",
      email: "you@example.com",
      request: "How can we pray for you?"
    },
    es: {
      name: "Tu nombre",
      email: "tucorreo@ejemplo.com",
      request: "¿Cómo podemos orar por ti?"
    }
  };

  document.getElementById("name").placeholder = placeholders[language].name;
  document.getElementById("email").placeholder = placeholders[language].email;
  document.getElementById("request").placeholder = placeholders[language].request;
}

function applyTranslations(language) {
  const content = translations[language];

  document.documentElement.lang = content.htmlLang;
  document.title = content.churchName;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (content[key]) {
      element.textContent = content[key];
    }
  });

  languageToggle.textContent = content.languageButton;
  updatePlaceholders(language);
  updateFundraising(language);
}

function toggleLanguage() {
  currentLanguage = currentLanguage === "en" ? "es" : "en";
  applyTranslations(currentLanguage);
}

languageToggle.addEventListener("click", toggleLanguage);

navToggle.addEventListener("click", () => {
  const isOpen = siteNav.classList.toggle("is-open");
  navToggle.setAttribute("aria-expanded", String(isOpen));
});

siteNav.querySelectorAll("a").forEach((link) => {
  link.addEventListener("click", () => {
    siteNav.classList.remove("is-open");
    navToggle.setAttribute("aria-expanded", "false");
  });
});

// Demo-only submission. Replace this with Formspree later.
prayerForm.addEventListener("submit", (event) => {
  event.preventDefault();

  const requestField = document.getElementById("request");

  if (!requestField.value.trim()) {
    formMessage.textContent = translations[currentLanguage].formError;
    return;
  }

  formMessage.textContent = translations[currentLanguage].formSuccess;
  prayerForm.reset();
});

applyTranslations(currentLanguage);
